package org.example;

// Query String Class -> 피연산자, 연산자를 추출하기 위한 작업
public class QueryString {
    // key variable
    private final String key;
    // value variable
    private final String value;


    public QueryString(String key, String value) {
        // key variable value 대입
        this.key = key;
        // value variable value 대입
        this.value = value;
    }
}
